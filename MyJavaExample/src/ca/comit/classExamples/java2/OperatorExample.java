package ca.comit.classExamples.java2;

public class OperatorExample {

	public static void main(String[] args) {

		/**
		 * Write a program in Java that prints the phrase "Hi, I already print
		 * sentences". Declares and prints an int number, for example 273, or 597.
		 * Declares and prints a float number, for example the 5.3, or the 7.5.
		 */
		int Number1 = 273;
		float Number2 = 5.3f;

		System.out.println("Hi, I already print sentences");
		System.out.println(Number1);
		System.out.println(Number2);

	}

}
